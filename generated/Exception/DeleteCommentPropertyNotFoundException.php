<?php

namespace JiraSdk\Exception;

class DeleteCommentPropertyNotFoundException extends NotFoundException
{
    /**
     * @var \Psr\Http\Message\ResponseInterface
     */
    private $response;
    public function __construct(\Psr\Http\Message\ResponseInterface $response = null)
    {
        parent::__construct('Returned if the comment or the property is not found or the user has the necessary project permissions but isn\'t a member of the role or group visibility of the comment is restricted to.');
        $this->response = $response;
    }
    public function getResponse(): ?\Psr\Http\Message\ResponseInterface
    {
        return $this->response;
    }
}
